package bg.tu_varna.sit.b1.f23621718.contracts.commands;

/**
 * Общ интерфейс за команди, които могат да бъдат изпълнени.
 *
 * <p>Този интерфейс дефинира основната структура на командите, като изисква
 * реализация на метода {@code execute()}, чрез който се изпълнява съответното действие.</p>
 *
 * <p>Използва се в контекста на Command дизайн шаблона.</p>
 */
public interface Command {
    /**
     * Изпълнява дефинираното действие на командата.
     */
    void execute();
}
