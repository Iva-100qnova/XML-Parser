package bg.tu_varna.sit.b1.f23621718.contracts.generators;

/**
 * Интерфейс, дефиниращ генератор за обекти от тип {@code T}.
 *
 * <p>Този интерфейс предоставя абстракция за създаване на нови инстанции от даден тип.</p>
 *
 * @param <T> Типът на обекта, който се генерира.
 */
public interface Generator<T> {

    /**
     * Генерира и връща нова инстанция от тип {@code T}.
     *
     * @return нова инстанция от тип {@code T}.
     */
    T generate();
}
