package bg.tu_varna.sit.b1.f23621718.contracts.parsers;

/**
 * Общ интерфейс за парсване на обекти от текстови вход.
 * <p>
 * Интерфейсът дефинира метод, който преобразува низ (String) в обект от тип {@code T}.
 * Използва се за създаване на обекти от текстово представяне – например парсване на параметри,
 * конфигурации, XML елементи и др.
 *
 * @param <T> Типът на обекта, който ще бъде върнат след парсване.
 */
public interface Parser<T> {

    /**
     * Парсира подаден низ и го преобразува в обект от тип {@code T}.
     *
     * @param s Низът за парсване.
     * @return Обект от тип {@code T}, създаден от подадения низ.
     * @throws IllegalArgumentException ако парсирането не може да бъде изпълнено успешно.
     */
    T parse(String s);
}